// ************* enum definitoin for type of lcd writing **************
// Enumeration for command/data transmission
typedef enum type{
  COMMAND = 0,
  DATA = 1
}write_t;

// ************* nokialcd_init **************
// Use the SPI initialization API to setup Nokia LCD
// Add the proper procedures according to LCD datasheet
// Initialize Nokia 5110 48x84 LCD by sending the proper
// commands to the PCD8544 driver.
// INPUT: none
// OUTPUT: none
// Assume system clock rate at 80 Mhz
void NOKIALCD_init(void);

// ************* nokialcd_write **************
// Function that sends a message to the LCD whether it is
// a command or data
// INPUT: Message type (command/data), 
//        message itself (8-bit to transmit)
// OUTPUT: none
void NOKIALCD_write(write_t, uint8_t);

// ************* nokialcd_send_char **************
// Print a character to the Nokia 5110 48x84 LCD.  The
// character will be printed at the current cursor position,
// the cursor will automatically be updated, and it will
// wrap to the next row or back to the top if necessary.
// One blank column of pixels will be printed on either side
// of the character for readability.  Since characters are 8
// pixels tall and 5 pixels wide, 12 characters fit per row,
// and there are six rows.
// INPUT: character data in byte length
// OUTPUT: none
void NOKIALCD_send_char(uint8_t);

// ************* nokialcd_send_string **************
// Print complete string on the LCD by sending it character 
// by character to Send char function.
// INPUT: a pointer to characters (string)
// OUTPUT: none
void NOKIALCD_send_string(uint8_t *);

// ************* nokialcd_send_decimal **************
// Output a 16-bit number in unsigned decimal format with a
// fixed size of five right-justified digits of output.
// INPUT: n, 16-bit unsigned decimal number
// OUTPUT: none
void NOKIALCD_send_decimal(uint8_t);

// ************* nokialcd_set_cursor **************
// Move the cursor to the desired X- and Y-position.  The
// next character will be printed here.  X=0 is the leftmost
// column.  Y=0 is the top row.
// INPUT: x position
//        y position of the 48x84 LCD
//        0 <= x <= 11
//        0 <= y <= 5
// OUTPUT: none
void NOKIALCD_set_cursor(uint8_t, uint8_t);

// ************* nokialcd_clear **************
// Clear the LCD by writing zeros to the entire screen and
// reset the cursor to (0,0) (top left corner of screen).
// INPUT: none
// OUTPUT: none
void NOKIALCD_clear(void);

// ************* nokialcd_draw_image **************
// Fill the whole screen by drawing a 48x84 bitmap image.
// INPUT: A pointer to 504 byte bitmap
// OUTPUT: none
void NOKIALCD_draw_image(const uint8_t *);

// ************* nokialcd_print_bitmap **************
// inputs:  coordination of the bitmap x position,
//          y position,
//          pointer to bitmap image to be printed,
//          threshold grayscale colors above this number make corresponding pixel 'on'
//           0 to 14.
// outputs: none.	   
void
NOKIALCD_print_bitmap(uint8_t x_pos, uint8_t y_pos,
	const uint8_t * p_bitmap, uint8_t threshold);

// ************* nokialcd_clear_buf **************
// There is a buffer in RAM that holds one screen
// This routine clears this buffer.
// INPUT: none
// OUTPUT: none
void NOKIALCD_clear_buf(void);

// ************* nokialcd_display_buf **************
// Fill the whole screen by drawing a 48x84 screen image.
// INPUT: none
// OUTPUT: none
void NOKIALCD_display_buf(void);

   
